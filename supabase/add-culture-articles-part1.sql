新手准备的材料：靛蓝染料粉或靛泥，纯棉白布或白T恤，橡皮筋或棉线，塑料手套，搅拌棒，塑料桶或染缸，围裙。这些材料在网上都能轻松买到。

最简单的染色步骤：将布料浸湿挤干，用橡皮筋随意扎结，准备染液按说明书配比，将扎好的布料浸入染液5-10分钟，取出在空气中氧化5分钟，重复浸染3-5次，拆开橡皮筋漂洗晾干。

新手常见问题：染色不均匀可能是浸染时间不够或染液浓度不均；颜色太浅说明浸染次数不够需多染几次；图案不清晰是扎结不够紧实；褪色严重可能没有充分氧化和固色。

进阶技巧：学习基本扎染手法如折叠、撮皱、捆扎；尝试简单图案从圆形、条纹开始；掌握色彩控制浸染次数决定深浅；了解不同面料棉麻丝反应各异。

安全注意事项：戴手套避免皮肤直接接触染料，通风操作保持空气流通，儿童操作需成人监护，染料存放远离食品。

